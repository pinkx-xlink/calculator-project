
//add function
function add(num1, num2) {
   let res = num1 + num2
   return res
}


//subtract function
function subtract(num1, num2) {
    let res = num1 - num2;
    return res
}


//multiply function
function multiply(num1, num2) {
    let res = num1 * num2;
    return res;
}


